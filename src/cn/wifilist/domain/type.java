package cn.wifilist.domain;

public class type {
	
	/**
	 * 一级名称
	 */
	private String name;
	/**
	 * 二级名称
	 */
	private String sub;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSub() {
		return sub;
	}
	public void setSub(String sub) {
		this.sub = sub;
	}
}
